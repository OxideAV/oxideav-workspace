//! MPEG-1 Audio 32-band polyphase synthesis filter bank.
//!
//! This is the same filter bank used by all three MPEG audio layers — see
//! ISO/IEC 11172-3 Annex B (§2.4.3.4 — Layer II "Subband synthesis"). Each
//! invocation consumes 32 subband samples (one per subband) and produces
//! 32 PCM output samples, while carrying a 1024-sample FIFO (`v`) of
//! matrixed values across calls.
//!
//! Algorithm (per channel):
//!   1. Shift `v[64..1024] <- v[0..960]`.
//!   2. Matrix: `v[i] = Σ_k N[i][k] * s[k]`, `i=0..63`, `k=0..31`,
//!      with `N[i][k] = cos((2k+1)(16+i) * π / 64)`.
//!   3. Build 512 `u[]` taps by interleaving `v[]`.
//!   4. Window: `w[i] = u[i] * D[i]` for `i=0..511`.
//!   5. Output: `pcm[j] = Σ_{i=0..15} w[32i + j]`, `j = 0..31`.
//!
//! The D[] window below is Table 3-B.3 / Annex D.1 of ISO/IEC 11172-3 —
//! 512 floating-point values quantised to multiples of 1/65536. Numerical
//! constants from an international standard are not copyrightable; the
//! same values appear in the `dist10` public-domain reference decoder, in
//! libmpg123 (LGPL), in `puremp3` (MIT) and elsewhere. They have been
//! spot-checked against libmpg123's `intwinbase` half-table.

use std::sync::OnceLock;

/// Per-channel synthesis state. The 1024-sample FIFO `v` accumulates
/// matrixed subband samples; it must persist across frames.
pub struct SynthesisState {
    v: Box<[f32; 1024]>,
}

impl Default for SynthesisState {
    fn default() -> Self {
        Self {
            v: Box::new([0.0; 1024]),
        }
    }
}

impl SynthesisState {
    pub fn new() -> Self {
        Self::default()
    }

    /// Feed 32 subband samples → emit 32 PCM samples (f32, roughly in
    /// `[-1.0, 1.0]`). Callers should multiply by 32767 and clamp to i16.
    pub fn synthesize(&mut self, subbands: &[f32; 32], out: &mut [f32; 32]) {
        // 1. Shift the FIFO down by 64.
        for i in (64..1024).rev() {
            self.v[i] = self.v[i - 64];
        }

        // 2. Matrix step: v[0..64] = N · subbands[0..32].
        let m = matrix();
        for i in 0..64 {
            let mut acc = 0.0f32;
            for k in 0..32 {
                acc += m[i][k] * subbands[k];
            }
            self.v[i] = acc;
        }

        // 3. Build the 512-sample u[] from v[].
        let mut u = [0.0f32; 512];
        for i in 0..8 {
            for j in 0..32 {
                u[64 * i + j] = self.v[128 * i + j];
                u[64 * i + 32 + j] = self.v[128 * i + 96 + j];
            }
        }

        // 4. Window with D[].
        for i in 0..512 {
            u[i] *= SYNTH_WINDOW_D[i];
        }

        // 5. Sum 16 windowed taps per output sample.
        for j in 0..32 {
            let mut acc = 0.0f32;
            for i in 0..16 {
                acc += u[32 * i + j];
            }
            out[j] = acc;
        }
    }
}

static MATRIX_STORAGE: OnceLock<[[f32; 32]; 64]> = OnceLock::new();

fn matrix() -> &'static [[f32; 32]; 64] {
    MATRIX_STORAGE.get_or_init(|| {
        let mut m = [[0.0f32; 32]; 64];
        let pi = std::f64::consts::PI;
        for i in 0..64 {
            for k in 0..32 {
                let angle = ((2 * k + 1) as f64) * ((16 + i) as f64) * pi / 64.0;
                m[i][k] = angle.cos() as f32;
            }
        }
        m
    })
}

