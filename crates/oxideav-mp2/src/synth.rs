#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn window_reference_values() {
        assert_eq!(SYNTH_WINDOW_D[0], 0.0);
        assert!((SYNTH_WINDOW_D[1] - -0.000015259).abs() < 1e-9);
        assert!((SYNTH_WINDOW_D[64] - 0.003250122).abs() < 1e-9);
        assert!((SYNTH_WINDOW_D[256] - 1.144989014).abs() < 1e-7);
        assert!((SYNTH_WINDOW_D[511] - 0.000015259).abs() < 1e-9);
    }

    #[test]
    fn window_has_512_entries() {
        assert_eq!(SYNTH_WINDOW_D.len(), 512);
    }

    #[test]
    fn silence_in_produces_silence_out() {
        let mut st = SynthesisState::new();
        let zero = [0.0f32; 32];
        let mut out = [0.0f32; 32];
        for _ in 0..16 {
            st.synthesize(&zero, &mut out);
        }
        for s in out.iter() {
            assert!(s.abs() < 1e-6);
        }
    }

    #[test]
    fn impulse_is_finite() {
        let mut st = SynthesisState::new();
        let mut s = [0.0f32; 32];
        s[0] = 1.0;
        let mut out = [0.0f32; 32];
        st.synthesize(&s, &mut out);
        for v in out.iter() {
            assert!(v.is_finite());
        }
    }

    /// DC in subband 0 should roughly reconstruct a constant signal after
    /// the filter settles. We feed a DC-0 impulse train and check that the
    /// output becomes quasi-stationary (absolute values bounded).
    #[test]
    fn dc_input_produces_bounded_output() {
        let mut st = SynthesisState::new();
        let mut s = [0.0f32; 32];
        s[0] = 0.5;
        let mut out = [0.0f32; 32];
        for _ in 0..32 {
            st.synthesize(&s, &mut out);
        }
        for v in out.iter() {
            assert!(v.abs() < 4.0, "bounded dc output: {v}");
        }
    }
}
