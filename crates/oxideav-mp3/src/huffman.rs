/// Decode one (x, y) big-value pair from the bit reader.
pub fn decode_pair(br: &mut BitReader<'_>, table_idx: u8) -> Result<(i32, i32)> {
    let t = &BIG_VALUE_TABLES[table_idx as usize];
    if t.tab.is_empty() {
        return Err(Error::invalid(format!(
            "MP3 Huffman: table {} is reserved / not yet populated",
            table_idx
        )));
    }
    let (x, y) = decode_symbol(br, t.tab)?;
    let mut x = x as i32;
    let mut y = y as i32;

    if t.linbits > 0 {
        if x == 15 {
            x += br.read_u32(t.linbits as u32)? as i32;
        }
        if y == 15 {
            y += br.read_u32(t.linbits as u32)? as i32;
        }
    }
    if x != 0 && br.read_bit()? {
        x = -x;
    }
    if y != 0 && br.read_bit()? {
        y = -y;
    }
    Ok((x, y))
}

/// Decode one count1 quadruple (v, w, x, y). Each coefficient is +-0 or +-1.
pub fn decode_count1(br: &mut BitReader<'_>, table_b: bool) -> Result<(i32, i32, i32, i32)> {
    let tab: HTab4 = if table_b { COUNT1_B } else { COUNT1_A };
    let (v, w, x, y) = decode_symbol4(br, tab)?;
    let mut v = v as i32;
    let mut w = w as i32;
    let mut x = x as i32;
    let mut y = y as i32;
    if v != 0 && br.read_bit()? {
        v = -v;
    }
    if w != 0 && br.read_bit()? {
        w = -w;
    }
    if x != 0 && br.read_bit()? {
        x = -x;
    }
    if y != 0 && br.read_bit()? {
        y = -y;
    }
    Ok((v, w, x, y))
}

/// Peek `max_len` bits, falling back to whatever bits remain (zero-padded
/// on the right) if the stream is near its end. Without this, a perfectly
/// valid short codeword that happens to be the very last thing in the
/// bitstream would error out spuriously.
fn peek_padded(br: &mut BitReader<'_>, max_len: u32) -> Result<u32> {
    if max_len == 0 {
        return Ok(0);
    }
    let avail = br.bits_remaining();
    if avail >= max_len as u64 {
        br.peek_u32(max_len)
    } else if avail == 0 {
        Err(Error::invalid("MP3 Huffman: stream exhausted"))
    } else {
        let n = avail as u32;
        let v = br.peek_u32(n)?;
        Ok(v << (max_len - n))
    }
}

fn decode_symbol(br: &mut BitReader<'_>, tab: HTab) -> Result<(u8, u8)> {
    let max_len = tab.iter().map(|e| e.1).max().unwrap_or(0) as u32;
    if max_len == 0 {
        return Ok((0, 0));
    }
    let peek_bits = peek_padded(br, max_len)?;
    for &(code, len, x, y) in tab {
        let l = len as u32;
        if (peek_bits >> (max_len - l)) == code {
            br.consume(l)?;
            return Ok((x, y));
        }
    }
    Err(Error::invalid("MP3 Huffman: no matching big-value code"))
}

pub type HTab4 = &'static [(u32, u8, u8, u8, u8, u8)];
fn decode_symbol4(br: &mut BitReader<'_>, tab: HTab4) -> Result<(u8, u8, u8, u8)> {
    let max_len = tab.iter().map(|e| e.1).max().unwrap_or(0) as u32;
    if max_len == 0 {
        return Ok((0, 0, 0, 0));
    }
    let peek_bits = peek_padded(br, max_len)?;
    for &(code, len, v, w, x, y) in tab {
        let l = len as u32;
        if (peek_bits >> (max_len - l)) == code {
            br.consume(l)?;
            return Ok((v, w, x, y));
        }
    }
    Err(Error::invalid("MP3 Huffman: no matching count1 code"))
}

