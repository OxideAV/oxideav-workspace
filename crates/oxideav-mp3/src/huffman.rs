    /// Verify Huffman prefix uniqueness for every populated big-value
    /// table: no two distinct (x, y) pairs share an extended prefix that
    /// would cause an ambiguous decode.
    #[test]
    fn all_big_value_tables_prefix_unique() {
        for (idx, t) in BIG_VALUE_TABLES.iter().enumerate() {
            let tab = t.tab;
            if tab.is_empty() {
                continue;
            }
            let max_len = tab.iter().map(|e| e.1 as u32).max().unwrap();
            let mut seen: std::collections::HashMap<u32, (u8, u8, u32, u8)> =
                std::collections::HashMap::new();
            for &(code, len, x, y) in tab {
                let l = len as u32;
                let span = 1u32 << (max_len - l);
                let base = code << (max_len - l);
                for ext in 0..span {
                    let pad = base | ext;
                    if let Some(prev) = seen.get(&pad) {
                        if prev.0 != x || prev.1 != y {
                            panic!(
                                "prefix conflict in table {idx}: pad=0x{pad:x} \
                                 prev=({},{}) code=0x{:x}/{} new=({x},{y}) code=0x{code:x}/{l}",
                                prev.0, prev.1, prev.2, prev.3
                            );
                        }
                    }
                    seen.insert(pad, (x, y, code, len));
                }
            }
        }
    }

