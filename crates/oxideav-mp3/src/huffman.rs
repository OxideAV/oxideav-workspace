//! MPEG-1/2 Layer III Huffman tables and decoder.
//!
//! 32 "big-value" tables (indices 0..32 with 4 and 14 reserved) plus the
//! two 4-value "count1" tables A and B. Each big-value code yields a
//! pair `(x, y)` of quantised coefficients; the count1 tables yield a
//! quadruple `(v, w, x, y)` each 0 or 1.
//!
//! For x or y == 15 when the table has `linbits > 0`, extra linbits
//! follow in the bitstream; then one sign bit per non-zero coefficient.
//!
//! Implementation: each big-value table is stored as a flat slice of
//! `(code, len, x, y)` rows. Decoding peeks `max_len` bits and linearly
//! searches for the first matching code. MP3 tables have `max_len <= 19`
//! so a single 32-bit peek suffices; ~600 lookups per granule × ~38
//! granules per second is comfortably handled by a linear scan.
//!
//! The numerical contents of these tables are facts from ISO/IEC
//! 11172-3 Annex B (Tables 3-B.7 through 3-B.24) and the count1 tables
//! 3-B.25 / 3-B.26. Reproducing standard table data is not subject to
//! copyright; this file uses our own representation.

